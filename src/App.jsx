function formatBN(v) {
  var n = v.replace(/\D/g, "").slice(0, 10);
  if (n.length <= 3) return n;
  if (n.length <= 5) return n.slice(0,3) + "-" + n.slice(3);
  return n.slice(0,3) + "-" + n.slice(3,5) + "-" + n.slice(5);
}

function validateBN(v) {
  var n = v.replace(/\D/g, "");
  if (n.length !== 10) return false;
  var w = [1,3,7,1,3,7,1,3,5];
  var s = 0;
  for (var i = 0; i < 9; i++) s += parseInt(n[i]) * w[i];
  s += Math.floor(parseInt(n[8]) * 5 / 10);
  return (10 - (s % 10)) % 10 === parseInt(n[9]);
}

